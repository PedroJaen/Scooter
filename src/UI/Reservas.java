    //rellenamos los campos
    private void rellenaCampos() {
        setElement(jComboBox_usuario,reservas.getId_usuario(),"usuario");
        setElement(jComboBox_scooter, reservas.getId_scooter(), "scooter");
        jSpinner_dia.setValue(reservas.getDia());
        jComboBox_inicio.setSelectedItem(reservas.getHora_inicio());
        jSpinner_horas.setValue(reservas.getHora_fin());
    }
    
    private void rellenaUsuarios() {
        try {
            //consulta
            Statement st=conn.createStatement();
            usuario=st.executeQuery("SELECT id,nombre,apellidos,dni FROM cliente");
            while(usuario.next()){
                Object[] fila = new Object[4];
                for (int i = 0; i < 4; i++) {
                    fila[i]=usuario.getObject(i+1);
                }
                jComboBox_usuario.addItem(fila[1]+" "+fila[2]+" "+fila[3]);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Reservas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void rellenaScooters() {
        try {
            //consulta
            Statement st=conn.createStatement();
            scooter=st.executeQuery("SELECT id,modelo,cilindrada FROM scooter");
            while(scooter.next()){
                Object[] fila = new Object[3];
                for (int i = 0; i < 3; i++) {
                    fila[i]=scooter.getObject(i+1);
                }
                jComboBox_scooter.addItem(fila[1]+" "+fila[2]);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Reservas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void rellenaHoraInicio() {
        for(int i=8;i<20;i++){
            jComboBox_inicio.addItem(Integer.toString(i));
        }
    }
    
    //obtenemos la fecha del spinner
    private String getFecha(JSpinner spinner){
        String fecha="";
        Date dia=(Date) spinner.getValue();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        fecha = sdf.format(dia);
        return fecha;
    }
    
    //a partir del id, rellenamos el campo correspondiente para que no aparezca el numero
    private void setElement(JComboBox combo,int id,String campo){
        try {
            //consulta
            Statement st=st=conn.createStatement();
            ResultSet rs;
            String cadena;
            if(campo.equals("usuario")){
                rs=st.executeQuery("SELECT nombre,apellidos,dni FROM cliente WHERE id="+id);
                rs.next();
                cadena=rs.getString(1)+" "+rs.getString(2)+" "+rs.getString(3);
            }
            else{
                rs=st.executeQuery("SELECT modelo,cilindrada FROM scooter WHERE id="+id);
                rs.next();
                cadena=rs.getString(1)+" "+rs.getString(2);
            }
            combo.setSelectedItem(cadena);
        } catch (SQLException ex) {
            Logger.getLogger(Reservas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    //obtenemos el id del elemento elegido en el combobox
    private int getId(JComboBox combo,ResultSet rs,String campo){
        int id=-1;
        String seleccion=combo.getSelectedItem().toString();
        //buscamos los nombres de producto en el resultser
        try {
            rs.beforeFirst();
            while(rs.next()){
                String currentElement;
                if(campo.equals("usuario")){
                    currentElement=rs.getString(2)+" "+rs.getString(3)+" "+rs.getString(4);
                }
                else{
                    currentElement=rs.getString(2)+" "+rs.getString(3);
                }
                
                //si coinciden, ponemos la marca
                if(seleccion.equals(currentElement)){
                    id=rs.getInt(1);
                    break;
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(Reservas.class.getName()).log(Level.SEVERE, null, ex);
        }
        return id;
    }
    
