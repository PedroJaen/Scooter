    private void jLabel_okMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel_okMouseClicked
        //obtenemos los datos
        int id_usuario=getId(jComboBox_usuario, usuario, "usuario");
        int id_scooter=getId(jComboBox_scooter, scooter, "scooter");
        String dia=getFecha(jSpinner_dia);
        int hora_inicio=Integer.parseInt((String) jComboBox_inicio.getSelectedItem());
        int hora_fin=(int) jSpinner_horas.getValue();
        
        if(hora_inicio+hora_fin>20){
            aviso("No se puede tener en alquiler mas tarde de las 20h");
        }
        else{
            //insertamos
            try {
                PreparedStatement ps=null;
                if(editar){
                    ps=conn.prepareStatement("UPDATE reserva SET id_usuario=?,id_scooter=?,dia=?,"
                            + "hora_inicio=?,hora_fin=? WHERE id="+reservas.getId());
                }
                else{
                    ps=conn.prepareStatement("INSERT INTO reserva(id_usuario,id_scooter,dia,hora_inicio,hora_fin) "
                            + "VALUES(?,?,?,?,?)");
                }
                ps.setInt(1,id_usuario);
                ps.setInt(2,id_scooter);
                ps.setString(3,dia);
                ps.setInt(4,hora_inicio);
                ps.setInt(5,hora_fin);
                ps.executeUpdate();
            } catch (SQLException ex) {
                Logger.getLogger(Reservas.class.getName()).log(Level.SEVERE, null, ex);
            }   
            dispose();
        }
        
    }//GEN-LAST:event_jLabel_okMouseClicked

